// DESCRIPTION : Initialisation de la carte : GPIO, Clocks, Interruptions...
//--------------------------------------------------------------------------------------------------
void OLED_Init(void)
{    
    delay(200);
    display.begin(0x3C);
    display.setTextColor(WHITE);
    display.clearDisplay();
    display.display();
}

void OLED_FillScreen(uint32_t color)
{
    display.clearDisplay();
}

DuinoShop_SSD1306 *OLED_Get(void)
{
    return &display;
}